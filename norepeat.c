#include<stdio.h>
int main()
{int a[100],n,i,j,count=0,k=0,b=0,s[50],e=0,x=0,p[50],o=0;
scanf("%d",&n);
for(i=0;i<n;i++)
{
scanf("%d",&a[i]);
}
for(i=0;i<n-1;i++)
{
for(j=i+1;j<n;j++)
{
if(a[i]==a[j])
{
count=count+1;
if(a[i]!=0)
{
s[k]=a[i];
k++;
}
}
}
}
b=count;
for(i=0;i<n;i++)
{
for(k=0;k<b;k++)
{
if(s[k]!=a[i])
{
e=e+1;
if(a[i]!=0)
{
p[x]=a[i];
x++;
}
}}
}
o=e;
if(o>1)
{
for(x=0;x<o;x++)
{
printf("%d\t",p[x]);
}
}}
